package main

import "fmt"

func main() {
	// Decimal
	fmt.Println(65)

	// Binary
	fmt.Printf("%b\n", 65)

	// Octa-decimal
	fmt.Printf("%o\n", 65)

	// Hexa-decimal
	fmt.Printf("%x\n", 65)

	// UTF-8 symbol
	fmt.Printf("%U\n", 65)

	// UTF-8 character
	fmt.Printf("%c\n", 65)

	// UTF-8 character (escaped)
	fmt.Printf("%q\n", 65)

	// UTF-8 code
	fmt.Println('A')
}
